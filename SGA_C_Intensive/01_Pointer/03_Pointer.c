// 장점 : 메모리 공간에 간접적으로 접근하여 해당 공간의 값을 변경할 수 있습니다.
// 단점 : 원본에 영향이 가는 방식이므로 보존성이 뛰어나지 않습니다.
// 사용하는 예 : 캐릭터 능력치
